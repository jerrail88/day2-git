# Day 5 - Exercise 1: Lists & Indexing

# Create a list of your 5 favorite foods
favorite_foods = ["Pizza", "Tacos", "Burgers", "Chicken Wings", "Pasta"]

# Print the first item
print("First item:", favorite_foods[0])

# Print the last item
print("Last item:", favorite_foods[-1])

# Print the entire list
print("Full list:", favorite_foods)

# Print the list in reverse
print("Reversed list:", favorite_foods[::-1])

# Add a new food to the list
favorite_foods.append("Sushi")
print("List after adding Sushi:", favorite_foods)
